function product
    (
        P_ID,
        P_name,
        P_Price,
        onSpecial,
        img,
        type,
        size,
        brand,
        description
    ) {
    this.P_ID = P_ID;
    this.P_name = P_name;
    this.P_Price = P_Price;
    this.onSpecial = onSpecial;
    this.img = img;
    this.type = type;
    this.size = size;
    this.brand = brand;
    this.description = description;
}